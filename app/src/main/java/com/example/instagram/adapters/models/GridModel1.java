package com.example.instagram.adapters.models;

public class GridModel1 {
    private int img;
    private int img1;
    private int img2;

    public GridModel1(int img, int img1, int img2) {
        this.img = img;
        this.img1 = img1;
        this.img2 = img2;
    }

    public int getImg() {
        return img;
    }

    public int getImg1() {
        return img1;
    }

    public int getImg2() {
        return img2;
    }
}
